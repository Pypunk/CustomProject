void Game::ProcessMouseUpEvent( const SDL_MouseButtonEvent& e )
{
	Point2f mousePos{ float(e.x),float(e.y) };
	switch ( e.button )
	{
	case SDL_BUTTON_LEFT:
		m_pLevel->AddTile(mousePos);
		break;
	}
}

